import UIKit

extension UIColor {
    static func lightSand() -> UIColor {
        return .init(red: 241/255, green: 239/255, blue: 229/255, alpha: 1)
    }
    
    static func accentGreen() -> UIColor {
        return .init(red: 85/255, green: 171/255, blue: 103/255, alpha: 1)
    }
    
    static func kingCrimson() -> UIColor {
        return .init(red: 198/255, green: 74/255, blue: 74/255, alpha: 1)
    }
}
